// Turns any test into the mocking spongebob meme
// flags for the ascii art with the text

use std::io;
use rand::Rng;
use std::env;

fn main() {

    welcome();

    let args: Vec<String> = env::args().collect();
    // dbg!(&args);

    let mut b_show_bob : bool = false;

    if args.len() > 2 {
        // arguments
        let option = args[2].clone()
                            .to_ascii_lowercase();

        match option.as_str() {
            "--h" => show_help(),
            "--help" => show_help(),
            "--s" => b_show_bob = true,
            "--show" => b_show_bob = true,
            _ => println!("Invalid input, ignoring and using default")
        }

    }


    println!("Enter text to Spongebobify:");

    let input_text = readin_line();

    println!("\n \n \n \n \n");

    let output_str = mod_text(input_text);

    println!("{}", output_str);

    if b_show_bob {print_bob()};

}


fn show_help() {
    // Prints help options

    println!("\
    ----------------------
Spongebobify, the CLI!
----------------------

Turns normal, boring text into the most mocking, biting attack immagineable.

Options:

|   '--h'/'--help'    | Shows the very text you are reading                  |
|   '--s'/'--show'    | Draws ascii art of mocking Spongebob along with text |
    ")
}

fn readin_line() -> String {
    // function for reading in data

    let mut input_text = String::new();

    io::stdin()
        .read_line(&mut input_text)
        .expect("Failed to read line");

    input_text.to_lowercase()
}


fn mod_text(strng : String) -> String {
    // putting text modification in own function

    // doing random for now
    let mut output_text = String::new();

    for (_, ltr) in strng.char_indices()
    {
        let chance = rand::rng().random_range(1..=100);

        if chance > 50
        {
            output_text.push(ltr.to_ascii_uppercase());
        } else {
            output_text.push(ltr.to_ascii_lowercase());
        }   
    }

    return output_text;
}


